// Rasterize the AgentDeck AD-shield logo (esp32/src/ui/assets/logo_64.png — the
// same asset the LVGL splash screens show) into a 1-bit-friendly alpha-mask C
// header for the InkDeck e-ink firmware. Companion to generate-creature-glyphs.mjs.
//
// The PNG is a dark badge with light shield/AD/circuitry marks. On 1-bit paper
// the badge fill reads as noise, so the mask keeps only the BRIGHT marks
// (luma > 150 → shield outline + "AD" letterforms) as ink — a clean line-art
// mark that matches what the color surfaces show.
//
// Output: esp32/src/ui/eink/logo_glyph_generated.h  (committed)
// Regenerate: pnpm generate-eink-logo

import sharp from 'sharp';
import { writeFileSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const srcPng = resolve(__dirname, '../esp32/src/ui/assets/logo_64.png');
const outFile = resolve(__dirname, '../esp32/src/ui/eink/logo_glyph_generated.h');

const SIZE = 64;
const LUMA_INK_THRESHOLD = 150;

const { data, info } = await sharp(srcPng)
  .greyscale()
  .resize(SIZE, SIZE, { fit: 'fill' })
  .raw()
  .toBuffer({ resolveWithObject: true });

const alpha = new Uint8Array(SIZE * SIZE);
let covered = 0;
for (let i = 0; i < SIZE * SIZE; i++) {
  const ink = data[i * info.channels] > LUMA_INK_THRESHOLD;
  alpha[i] = ink ? 255 : 0;
  if (ink) covered++;
}

const lines = [];
for (let y = 0; y < SIZE; y++) {
  const row = [];
  for (let x = 0; x < SIZE; x++) row.push(String(alpha[y * SIZE + x]));
  lines.push('    ' + row.join(',') + ',');
}

const header = `// AUTO-GENERATED by scripts/generate-eink-logo.mjs — DO NOT EDIT.
// Run \`pnpm generate-eink-logo\` after changing esp32/src/ui/assets/logo_64.png.
// AD-shield brand mark as an 8-bit ink-coverage mask (bright marks only) for
// 1-bit e-ink rendering. Same asset the LVGL splash screens display in color.
#pragma once
#include <cstdint>

namespace LogoGlyph {

constexpr int AD_SHIELD_W = ${SIZE};
constexpr int AD_SHIELD_H = ${SIZE};
static const uint8_t AD_SHIELD_A8[${SIZE} * ${SIZE}] = {
${lines.join('\n')}
};

}  // namespace LogoGlyph
`;

writeFileSync(outFile, header);
console.log(`Wrote ${outFile} (${(header.length / 1024).toFixed(1)} KB, ${covered} ink px)`);
