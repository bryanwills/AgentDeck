// Generate the Swift mirror of the Timebox Mini 11×11 micro-glyph data from the
// TS SSOT (bridge/src/pixoo/micro-glyphs.ts), so the App Store macOS daemon and the
// Node CLI can never drift. Mirrors the generate-creature-glyphs.mjs codegen pattern.
//
// micro-glyphs.ts stays the single source of truth (it is imported at runtime by the
// compiled bridge — the bridge runs from dist with plain `tsc`, which does not copy a
// JSON sidecar, so the data must remain inline TS). This script parses the four glyph
// object literals out of that file and emits the consuming Swift data table.
//
// Output: apple/AgentDeck/Daemon/Modules/MicroGlyphs.generated.swift (committed).
// After running, the hand-written MicroGlyphs.swift consumes `generatedGlyphs`.
//
//   pnpm generate-micro-glyphs

import { writeFileSync, readFileSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';
import sharp from 'sharp';

const __dirname = dirname(fileURLToPath(import.meta.url));
const srcFile = resolve(__dirname, '../bridge/src/pixoo/micro-glyphs.ts');
const outFile = resolve(__dirname, '../apple/AgentDeck/Daemon/Modules/MicroGlyphs.generated.swift');
const tsMaskOutFile = resolve(__dirname, '../bridge/src/pixoo/official-dot-glyphs.generated.ts');
const cppMaskOutFile = resolve(__dirname, '../esp32/src/ui/matrix/official_dot_glyphs_generated.h');

const OFFICIAL_GLYPHS = [
  ['claudeCode', 'claudecode'],
  ['codex', 'codex'],
  ['openCode', 'opencode'],
  ['openClaw', 'openclaw'],
  ['antigravity', 'antigravity'],
];
const STANDARD_MASK_SIZE = 24;
const TC001_MASK_SIZE = 8;

// TS const name → TS MicroCreature key (the renderer mapping in micro-glyphs.ts GLYPHS).
const CONSTS = [
  ['OCTOPUS', 'octopus'],
  ['JELLYFISH', 'jellyfish'],
  ['OPENCODE', 'opencode'],
  ['CRAYFISH', 'crayfish'],
  ['ANTIGRAVITY', 'antigravity'],
];

const ts = readFileSync(srcFile, 'utf8');

/** Extract `const NAME: Glyph = { ... };` and evaluate the object literal. */
function extractGlyph(name) {
  // Each glyph block ends at the first line that is exactly `};` — no inner content
  // contains that sequence, so the non-greedy match is unambiguous.
  const re = new RegExp(`const ${name}: Glyph = (\\{[\\s\\S]*?\\n\\});`, 'm');
  const m = ts.match(re);
  if (!m) throw new Error(`generate-micro-glyphs: could not find glyph const ${name} in micro-glyphs.ts`);
  // The literal is plain JS (unquoted keys, number-array colors) — eval our own source.
  // eslint-disable-next-line no-new-func
  const glyph = new Function(`return (${m[1]})`)();
  if (!Array.isArray(glyph.idle) || glyph.idle.length !== 11) {
    throw new Error(`generate-micro-glyphs: ${name}.idle must be 11 rows`);
  }
  return glyph;
}

function swiftRows(rows) {
  return rows.map((r) => `            ${JSON.stringify(r)},`).join('\n');
}

function swiftColors(colors) {
  const entries = Object.entries(colors).map(([k, [r, g, b]]) => `"${k}": (${r}, ${g}, ${b})`);
  return `[${entries.join(', ')}]`;
}

function swiftGlyph(key, glyph) {
  const work = glyph.work
    ? `\n        work: [\n${swiftRows(glyph.work)}\n        ]`
    : '\n        work: nil';
  return `    "${key}": Glyph(
        colors: ${swiftColors(glyph.colors)},
        idle: [
${swiftRows(glyph.idle)}
        ],${work}
    ),`;
}

const bodies = CONSTS.map(([constName, key]) => swiftGlyph(key, extractGlyph(constName))).join('\n');

async function rasterizeOfficialGlyph(fileStem, size) {
  const svg = readFileSync(resolve(__dirname, `../design/brand/${fileStem}.svg`), 'utf8')
    .replaceAll('currentColor', '#ffffff');
  const { data, info } = await sharp(Buffer.from(svg), { density: 384 })
    .resize(size, size, { fit: 'fill' })
    .ensureAlpha()
    .raw()
    .toBuffer({ resolveWithObject: true });
  const alpha = [];
  for (let i = 0; i < size * size; i++) alpha.push(data[i * info.channels + info.channels - 1]);
  return alpha;
}

const officialMasks = {};
for (const [key, fileStem] of OFFICIAL_GLYPHS) {
  officialMasks[key] = {
    standard: await rasterizeOfficialGlyph(fileStem, STANDARD_MASK_SIZE),
    tc001: await rasterizeOfficialGlyph(fileStem, TC001_MASK_SIZE),
  };
}

function wrapNumbers(values, indent, perLine = 24) {
  const lines = [];
  for (let i = 0; i < values.length; i += perLine) {
    lines.push(`${indent}${values.slice(i, i + perLine).join(', ')},`);
  }
  return lines.join('\n');
}

const tsMasks = OFFICIAL_GLYPHS.map(([key]) => `  ${key}: new Uint8Array([\n${wrapNumbers(officialMasks[key].standard, '    ')}\n  ]),`).join('\n');
const tsTc001Masks = OFFICIAL_GLYPHS.map(([key]) => `  ${key}: new Uint8Array([\n${wrapNumbers(officialMasks[key].tc001, '    ', 8)}\n  ]),`).join('\n');
const tsMaskSource = `// AUTO-GENERATED by scripts/generate-micro-glyphs.mjs — DO NOT EDIT.\n// Canonical source: design/brand/*.svg. Regenerate with: pnpm generate-micro-glyphs\n\nexport type OfficialDotGlyphName = 'claudeCode' | 'codex' | 'openCode' | 'openClaw' | 'antigravity';\nexport const OFFICIAL_DOT_GLYPH_SIZE = ${STANDARD_MASK_SIZE};\nexport const OFFICIAL_DOT_GLYPHS: Record<OfficialDotGlyphName, Uint8Array> = {\n${tsMasks}\n};\n\nexport const OFFICIAL_TC001_GLYPH_SIZE = ${TC001_MASK_SIZE};\nexport const OFFICIAL_TC001_GLYPHS: Record<OfficialDotGlyphName, Uint8Array> = {\n${tsTc001Masks}\n};\n`;
writeFileSync(tsMaskOutFile, tsMaskSource);

const swiftMasks = OFFICIAL_GLYPHS.map(([key]) => `        .${key}: [\n${wrapNumbers(officialMasks[key].standard, '            ')}\n        ],`).join('\n');
const swiftTc001Masks = OFFICIAL_GLYPHS.map(([key]) => `        .${key}: [\n${wrapNumbers(officialMasks[key].tc001, '            ', 8)}\n        ],`).join('\n');

function cppRows(key, values) {
  const upper = key.replace(/([A-Z])/g, '_$1').toUpperCase();
  return `static const uint8_t ${upper}[SIZE * SIZE] = {\n${wrapNumbers(values, '    ', 8)}\n};`;
}
const cppMasks = OFFICIAL_GLYPHS.map(([key]) => cppRows(key, officialMasks[key].tc001)).join('\n\n');
const cppMaskSource = `// AUTO-GENERATED by scripts/generate-micro-glyphs.mjs — DO NOT EDIT.\n// Canonical source: design/brand/*.svg. Regenerate with: pnpm generate-micro-glyphs\n#pragma once\n#include <cstdint>\n\nnamespace OfficialDotGlyphs {\nconstexpr int SIZE = ${TC001_MASK_SIZE};\n\n${cppMasks}\n\n}  // namespace OfficialDotGlyphs\n`;
writeFileSync(cppMaskOutFile, cppMaskSource);

const header = `#if os(macOS)
// MicroGlyphs.generated.swift — AUTO-GENERATED, DO NOT EDIT.
//
// Source of truth: bridge/src/pixoo/micro-glyphs.ts
// Regenerate with: pnpm generate-micro-glyphs
//
// The grid/color tables below are emitted byte-for-byte from the TS glyph literals so
// the App Store macOS daemon and the Node CLI render identical Timebox Mini frames.
// Edit micro-glyphs.ts and re-run the generator — never hand-edit this file.

import Foundation

enum OfficialDotGlyph: Hashable {
    case claudeCode, codex, openCode, openClaw, antigravity
}

enum OfficialDotGlyphs {
    static let size = ${STANDARD_MASK_SIZE}
    static let masks: [OfficialDotGlyph: [UInt8]] = [
${swiftMasks}
    ]
}

enum OfficialTc001Glyphs {
    static let size = ${TC001_MASK_SIZE}
    static let masks: [OfficialDotGlyph: [UInt8]] = [
${swiftTc001Masks}
    ]
}

extension MicroGlyphs {
    // Keyed by the TS MicroCreature names; MicroGlyphs.glyph(for:) maps the Swift enum
    // (note: Swift .codex == TS "jellyfish").
    static let generatedGlyphs: [String: Glyph] = [
${bodies}
    ]
}
#endif
`;

writeFileSync(outFile, header);
console.log(`generate-micro-glyphs: wrote Timebox data + ${OFFICIAL_GLYPHS.length} canonical dot-matrix masks`);
